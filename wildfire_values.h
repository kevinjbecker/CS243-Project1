#ifndef WILDFIRE_VALUES
// include guard
#define WILDFIRE_VALUES

// the character for an ``alive'' tree
#define ALIVE 'Y'
// the character for a ``burning'' tree
#define BURNING '*'
// the character for a ``burnt'' tree
#define BURNT '_'
// the character for an ``empty'' cell
#define EMPTY ' '

// the value of an empty space
#define EMPTY_VALUE 0
// the value of an alive tree
#define ALIVE_VALUE 1
// the value of a burning tree
#define BURNING_VALUE 2
// the value of a burnt tree
#define BURNT_VALUE 3

#endif
